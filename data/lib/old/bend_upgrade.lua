function getStonesForNextUpgrade(cid)
	local newLevel = getPlayerStorageValue(cid, 63719)
	if newLevel <= 0 then 
		newLevel = 0
	end
	newLevel = newLevel+1
	if newLevel <= 4 then 
		return 1
	elseif newLevel <= 6 then
		return 2
	elseif newLevel <= 8 then 
		return 3
	elseif newLevel <= 10 then 
		return 4
	elseif newLevel <= 12 then 
		return 5
	elseif newLevel <= 13 then 
		return 7
	elseif newLevel <= 14 then 
		return 8
	elseif newLevel == 15 then
		return 10
	elseif newLevel == 16 then
		return 12
	elseif newLevel == 17 then
		return 15
	elseif newLevel == 18 then
		return 20
	else
		return 30
	end
end

function getMessageFromUpgrade(elem, level)
	if elem == 1 then -- fire
		return fireStones[level]
	elseif elem == 2 then -- water
		return waterStones[level]
	elseif elem == 3 then  -- air
		return airStones[level]
	else  -- earth
		return earthStones[level]
	end
end

function getDobrasLevel(cid)
	local level = getPlayerStorageValue(cid, 63719)
	if level <= 0 then 
		level = 0
	end
	return level
end

function addDobrasLevel(cid)
	local level = getPlayerStorageValue(cid, 63719)
	if level <= 0 then 
		level = 0
	end
	return setPlayerStorageValue(cid, 63719, level+1)
end

